// Helper functions
export const getCountryByCode = (code: string): Country | undefined => {
  return countries.find(country => country.code === code);
};

export const getCountryByName = (name: string): Country | undefined => {
  return countries.find(country => country.name === name);
};

export const getCitiesByCountryCode = (code: string): string[] => {
  return cities[code] || [];
};

export const getCitiesByCountryName = (countryName: string): string[] => {
  const country = getCountryByName(countryName);
  return country ? getCitiesByCountryCode(country.code) : [];
};

export const searchCountries = (query: string): Country[] => {
  if (!query) return countries;
  
  const lowerQuery = query.toLowerCase();
  return countries.filter(country => 
    country.name.toLowerCase().includes(lowerQuery) ||
    country.code.toLowerCase().includes(lowerQuery)
  );
};

export const searchCities = (query: string, countryCode?: string): string[] => {
  if (!query) return countryCode ? getCitiesByCountryCode(countryCode) : [];
  
  const lowerQuery = query.toLowerCase();
  const targetCities = countryCode ? getCitiesByCountryCode(countryCode) : 
    Object.values(cities).flat();
  
  return targetCities.filter(city => 
    city.toLowerCase().includes(lowerQuery)
  );
};

export default {
  countries,
  cities,
  getCountryByCode,
  getCountryByName,
  getCitiesByCountryCode,
  getCitiesByCountryName,
  searchCountries,
  searchCities,
};